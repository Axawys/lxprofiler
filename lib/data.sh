# Возвращает описание класса с учётом процента наполнения
describe() {
  local key=$1 pct=$2
  if   (( pct >= 76 )); then printf '%s' "${DESC_HIGH[$key]}"
  elif (( pct >= 51 )); then printf '%s' "${DESC_MID[$key]}"
  elif (( pct >= 21 )); then printf '%s' "${DESC_LOW[$key]}"
  else                       printf '%s' "${DESC_NONE[$key]}"
  fi
}
